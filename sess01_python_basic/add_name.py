#Python script to get the numbers from the user add them and display their sum

#Prompt/ask the user for the first number
num1 = int(input('Enter a number: \n'))

#Prompt/ask the user for the second number
num2 = int(input('Enter another number: \n'))

#add the two numbers
sum = num1 + num2

#display the result
print(f'Sum of {num1} + {num2} = {sum}')